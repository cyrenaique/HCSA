        public static string ConvertIntPosToStringPos(int Pos)
        {
            string ReturnString = "";
            int First = Pos / 27;
            int Second = Pos - First * 26;
            if (First != 0) ReturnString = ((char)(First + 64)).ToString();
            if (Second != 0) ReturnString += ((char)(Second + 64)).ToString();

            return ReturnString;
        }

